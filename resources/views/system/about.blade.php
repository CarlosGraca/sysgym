        <p>
            <b>OdontSoft</b> é um sistema de gestão de clínica dentária, desenvolvido com objectivo
            de ajudar a melhorar a qualidade de atendimento dos pacientes num clinica de odontologia
        </p>
        <p>
            O sistema foi desenvolvido no Framework <a href="https://laravel.com" target="_blank">Laravel</a>, esse sistema
            foi desenvolvido pensando na segurança, usabilidade, disponibilidade e sobretudo nos pacientes e na Clientes.
        </p>
        <br>
        <p>
            Especificações do Sistema:
        <ul>
            <li>Registo dos Pacientes </li>
            <li>Registo dos Funcionários </li>
            <li>Registo das Entidades Seguradoras </li>
            <li>Registo de Comparticipação das Consultas pelas Entidades Seguradoras </li>
            <li>Registo das Filiais </li>
            <li>Registo dos Tipos de Consulta e seu preço </li>
            <li>Controlo de Acesso </li>
            <li>Gerar Utilizadores </li>
            <li>Agenda de Consultas via calendário </li>
            <li>Orçamento de Consultas </li>
            <li>Relatórios </li>
            <li>Dashboards Inteligentes </li>
            <li>Escolha de Temas </li>
            <li>Escolha do Idioma </li>
            <li>Escolha da Moeda </li>
            <li>Escolha do Layout </li>
            <li>Escolha da Imagem de Fundo </li>
        </ul>

        </p>
